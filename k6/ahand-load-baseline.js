// ahand load baseline
//
// Phase 9 / Task 9.5 — k6 baseline that pumps two scenarios at the
// production-shaped surfaces:
//
//   1. webhook_throughput  — POST signed webhook envelopes to the
//      team9 gateway's `/api/v1/ahand/hub-webhook`. Targets the same
//      ingress path the ahand-hub uses for `device.heartbeat` /
//      `device.online` / `device.offline`.
//   2. control_plane_spawn — POST `CreateJobRequest` bodies to the
//      hub's `/api/control/jobs`. The hub responds 202 + `{jobId}`
//      after dispatching the envelope to the device WebSocket; we
//      don't subscribe to the SSE stream here (a separate k6 + WS
//      run is the right shape if SSE-side latency ever needs a
//      baseline).
//
// The third scenario in the spec — 1000 connected daemons — isn't
// expressible in k6's HTTP executor; it needs `k6/x/websockets` or a
// custom `ahandd` driver. Out of scope for the baseline run.
//
// This script is RUN MANUALLY (or via a nightly CI job), NEVER in PR
// CI. The thresholds in `options` are a regression smoke for
// before/after deltas, not absolute SLOs.
//
// Pre-requisites:
//
//   1. Pre-sign a batch of webhook payloads:
//
//        node k6/scripts/gen-webhook-payloads.mjs > /tmp/webhook-payloads.json
//
//      The script reads `AHAND_HUB_WEBHOOK_SECRET` from the
//      environment and emits 1000 fresh signed payloads (mix of
//      heartbeat + online + offline) as a JSON array.
//
//   2. Mint a control-plane JWT:
//
//        export CP_JWT=$(./k6/scripts/mint-cp-jwt.sh "$EXTERNAL_USER_ID")
//
//      The mint script is environment-specific (it lives next to the
//      hub-side jwt secret), so it isn't shipped here. For dev, use
//      `aws ssm get-parameter` against the prod-mirror env to fetch
//      the secret then sign with `jose` or your preferred jwt CLI.
//
//   3. Have a real `TEST_DEVICE_ID` registered + online on the hub.
//      The k6 script verifies the env var is populated; if your dev
//      hub doesn't have a daemon online, set `SKIP_SPAWN_SCENARIO=1`.
//
// Run:
//
//   GATEWAY_URL=https://gateway.dev.team9.ai \
//   HUB_URL=https://ahand-hub.dev.team9.ai \
//   CP_JWT=... \
//   TEST_DEVICE_ID=$(...) \
//   WEBHOOK_PAYLOADS_JSON=/tmp/webhook-payloads.json \
//   k6 run k6/ahand-load-baseline.js --out json=/tmp/k6-report.json
//
// The JSON report is meant to be uploaded as a CI artifact for
// before/after diffing.

import http from "k6/http";
import { check, sleep } from "k6";
import { SharedArray } from "k6/data";

// ── Config ──────────────────────────────────────────────────────────────────

const GATEWAY_URL = (__ENV.GATEWAY_URL ?? "").replace(/\/+$/, "");
const HUB_URL = (__ENV.HUB_URL ?? "").replace(/\/+$/, "");
const CP_JWT = __ENV.CP_JWT;
const TEST_DEVICE_ID = __ENV.TEST_DEVICE_ID;
const WEBHOOK_PAYLOADS_JSON = __ENV.WEBHOOK_PAYLOADS_JSON;
const SKIP_SPAWN_SCENARIO = __ENV.SKIP_SPAWN_SCENARIO === "1";
const SKIP_WEBHOOK_SCENARIO = __ENV.SKIP_WEBHOOK_SCENARIO === "1";

if (!GATEWAY_URL && !SKIP_WEBHOOK_SCENARIO) {
  throw new Error(
    "GATEWAY_URL is required for the webhook scenario. Set SKIP_WEBHOOK_SCENARIO=1 to skip it.",
  );
}
if (!HUB_URL && !SKIP_SPAWN_SCENARIO) {
  throw new Error(
    "HUB_URL is required for the control-plane scenario. Set SKIP_SPAWN_SCENARIO=1 to skip it.",
  );
}
if (!SKIP_SPAWN_SCENARIO) {
  if (!CP_JWT) throw new Error("CP_JWT is required for the spawn scenario.");
  if (!TEST_DEVICE_ID) {
    throw new Error("TEST_DEVICE_ID is required for the spawn scenario.");
  }
}

// `SharedArray` runs its callback exactly once per VU group and shares
// the result across VUs to avoid loading the file 1k times. It also
// emits memory-friendly proxies, so accessing items is cheap even with
// thousands of VUs.
const payloads = SKIP_WEBHOOK_SCENARIO
  ? new SharedArray("webhooks", () => [])
  : new SharedArray("webhooks", () => {
      if (!WEBHOOK_PAYLOADS_JSON) {
        throw new Error(
          "WEBHOOK_PAYLOADS_JSON must point to a JSON file generated by k6/scripts/gen-webhook-payloads.mjs",
        );
      }
      const raw = open(WEBHOOK_PAYLOADS_JSON);
      const parsed = JSON.parse(raw);
      if (!Array.isArray(parsed) || parsed.length === 0) {
        throw new Error(
          "WEBHOOK_PAYLOADS_JSON must be a non-empty JSON array of {body,signature,timestamp,eventId} objects",
        );
      }
      return parsed;
    });

// ── Scenario plan ──────────────────────────────────────────────────────────

const scenarios = {};
const thresholds = {};

if (!SKIP_WEBHOOK_SCENARIO) {
  scenarios.webhook_throughput = {
    executor: "constant-arrival-rate",
    // Spec § 9.4.11 calls for 10k webhooks/s. Production prod gateway
    // is multi-pod; the baseline target is "1k/s sustained from a
    // single k6 host with reasonable headroom" — the multiplier to
    // 10k is a horizontal-scale property of the deployment, not of
    // the gateway code.
    rate: Number(__ENV.WEBHOOK_RATE ?? 1000),
    timeUnit: "1s",
    duration: __ENV.WEBHOOK_DURATION ?? "1m",
    preAllocatedVUs: Number(__ENV.WEBHOOK_PREALLOCATED_VUS ?? 200),
    maxVUs: Number(__ENV.WEBHOOK_MAX_VUS ?? 800),
    exec: "webhookScenario",
  };
  thresholds["http_req_duration{scenario:webhook_throughput}"] = [
    "p(95)<100",
    "p(99)<200",
  ];
  thresholds["http_req_failed{scenario:webhook_throughput}"] = ["rate<0.01"];
}

if (!SKIP_SPAWN_SCENARIO) {
  scenarios.control_plane_spawn = {
    executor: "constant-vus",
    vus: Number(__ENV.SPAWN_VUS ?? 100),
    duration: __ENV.SPAWN_DURATION ?? "2m",
    exec: "spawnScenario",
    // Stagger the spawn scenario start so the webhook scenario gets a
    // clean ramp first — easier to read the report.
    startTime: __ENV.SPAWN_START_TIME ?? "10s",
  };
  thresholds["http_req_duration{scenario:control_plane_spawn}"] = [
    "p(95)<300",
    "p(99)<500",
  ];
  thresholds["http_req_failed{scenario:control_plane_spawn}"] = ["rate<0.02"];
}

export const options = {
  // Bail early if either scenario starts erroring out hard — a
  // 50%+ failure rate in the first 10s usually means CP_JWT is
  // wrong or the device went offline, and continuing for 2 minutes
  // wastes test time.
  abortOnFail: true,
  scenarios,
  thresholds,
};

// ── Webhook scenario ───────────────────────────────────────────────────────

export function webhookScenario() {
  const p = payloads[Math.floor(Math.random() * payloads.length)];
  const res = http.post(`${GATEWAY_URL}/api/v1/ahand/hub-webhook`, p.body, {
    headers: {
      "content-type": "application/json",
      "x-ahand-signature": p.signature,
      "x-ahand-timestamp": p.timestamp,
      "x-ahand-event-id": p.eventId,
    },
    tags: { scenario: "webhook_throughput" },
  });
  check(res, {
    // 204 = accepted + queued; the gateway never returns 200 for the
    // webhook ingress (per controller). Anything else is a regression
    // worth investigating.
    "status is 204": (r) => r.status === 204,
  });
}

// ── Spawn scenario ─────────────────────────────────────────────────────────

export function spawnScenario() {
  const body = JSON.stringify({
    deviceId: TEST_DEVICE_ID,
    // `true` is a no-op binary that returns immediately. Picking the
    // smallest possible job keeps the test about hub queueing latency
    // — not about the daemon's process-spawn cost.
    tool: "true",
    correlationId: `k6-${__VU}-${__ITER}`,
  });
  const res = http.post(`${HUB_URL}/api/control/jobs`, body, {
    headers: {
      "content-type": "application/json",
      authorization: `Bearer ${CP_JWT}`,
    },
    tags: { scenario: "control_plane_spawn" },
  });
  check(res, {
    "status is 200 or 202": (r) => r.status === 200 || r.status === 202,
  });
  // The spec asks for "100 concurrent agent spawns", but
  // constant-vus + a per-iteration sleep models throughput more
  // realistically (a real consumer batches its work into bursts).
  // Without the sleep, 100 VUs would saturate the hub at unbounded
  // QPS and the report tells us nothing about per-request latency
  // under a realistic call cadence.
  sleep(0.1);
}
